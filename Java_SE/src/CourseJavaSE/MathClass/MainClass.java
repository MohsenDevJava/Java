package CourseJavaSE.MathClass;

public class MainClass {

    public static void main(String[] args) {

        System.out.println(Math.abs(-12.553));
        System.out.println(Math.abs(45));
        System.out.println(Math.ceil(23.144));
        System.out.println(Math.floor(43.6454));
        System.out.println(Math.round(34.231));
        System.out.println(Math.round(34.631));
        System.out.println(Math.pow(12,3));
        System.out.println(Math.min(5,6));
        System.out.println(Math.max(9,8));
        System.out.println(Math.toRadians(180));

        System.out.println(Math.sin(Math.toRadians(90)));
        System.out.println(Math.cos(Math.toRadians(45)));
        System.out.println(Math.tan(Math.toRadians(90)));
    }
}
